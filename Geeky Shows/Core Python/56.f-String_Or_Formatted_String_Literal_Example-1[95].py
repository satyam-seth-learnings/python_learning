# Integer
print("*** Integer ***")
a=10
b=20
print(f"{a}")
print(f"{a} {b}")
print(f"{a}{b}")
print(f"{b} {a}")
value=f"{a}"
print(value)
# Float
print("*** Float ***")
c=10.56
d=20.42
print(f"{c}")
print(f"{c} {d}")
print(f"{d} {c}")
# String
print("*** String ***")
fname="Satyam"
lname="Seth"
print(f"{fname}")
print(f"{fname} {lname}")
print(f"{lname} {fname}")
# Integer And String
print("*** Mixed ***")
name="Geeky"
age=10
print(f"My name is {name}")
print(f"{name} {age}")
print(f"{age} {name}")