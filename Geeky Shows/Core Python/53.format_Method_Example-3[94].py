# Float
print("********** Float ***********")
print("{}".format(15.65))
print("{:f}".format(15.65))
print("{0:f}".format(15.65))
print("{num:f}".format(num=15.65))
print("{num:8f}".format(num=15.65))
print("{num:8.3f}".format(num=15.65))
print("{num:+8.2f}".format(num=15.65))
print("{num:<8.2f}".format(num=15.65))
print("{num:*<8.2f}".format(num=15.65))
print("{num:*>8.2f}".format(num=15.65))
print("{num:^8.2f}".format(num=15.65))
print("{num:*^8.2f}".format(num=15.65))